extern uint8_t threshold[5]; /*5 characters 'e.g 25.25', and one char for \r*/
int Thresh_integer_part=0;
int Thresh_fraction_part=0;
//extern int Thresh_integer_part;
//extern int Thresh_fraction_part;

int flag1=0;
extern int intr_flag;
extern int i;
void setThreshold() /*convert threshold to decimal*/
{
	/*parse the user input and convert it to numbers*/
	if(intr_flag==1){
	Thresh_integer_part = (threshold[0]-'0')*10 + (threshold[1]-'0');
	Thresh_fraction_part = ((threshold[3]-'0')*10 + (threshold[4]-'0'))/25; /*resolution of 0.25*/
	i=0;
	flag1=1;
	}
}

uint8_t temp_intPart[2];
uint8_t temp_fracPart[2];

int outputFrac, outputInt; /*final output as decimals*/
uint8_t output[7];
int flag2=0;
uint8_t msg[20] ="Current Temp = ";
void ReadTemp()
{

	HAL_I2C_Master_Transmit(&hi2c1, 0xD0, temp_intPart, 1, 10); /*transmit address of upper temp register*/
	HAL_I2C_Master_Receive(&hi2c1, 0xD1, temp_intPart+1, 1, 10); /*receive data "the integer part"*/

	HAL_I2C_Master_Transmit(&hi2c1, 0xD0, temp_fracPart, 1, 10); /*transmit address of lower temp register*/
	HAL_I2C_Master_Receive(&hi2c1, 0xD1, temp_fracPart+1, 1, 10); /*receive data "the fraction part"*/
	
	outputFrac = temp_fracPart[1] & 0xc0; /*0xc00 = 0b11000000 , get the most significant two bits*/
	outputFrac = outputFrac >> 6;
	outputInt=temp_intPart[1];
	output[0]=(temp_intPart[1]/10) + '0';
	output[1]=(temp_intPart[1]%10) + '0';
	output[2]='.';
	output[3]=outputFrac*25/10 + '0';
	output[4]=outputFrac*25%10 + '0';
	output[5]='\r';
	output[6]='\n';
	HAL_UART_Transmit(&huart1, msg, 20, 5000); /*transmit it to UART*/

	HAL_UART_Transmit(&huart1, output, 7, 5000); /*transmit it to UART*/
	flag2=1;
	
	ReRunMe(100); /*rerun every 30 sec*/
	compareToThreshold();
}

